// 借助 Acc 遍历联合类型的每一个元素，逐个添加到结果数组中，剩余的元素使用 Exclude 插入即可
type Permutation<T, Acc = T> =
  // base case
  [T] extends [never]
    ? []
    : // iterates item of union type `T`
    Acc extends T
    ? [Acc, ...Permutation<Exclude<T, Acc>>]
    : []

